    public static class Quote {
        private final int quote, name;
        private final CharacterQuote[] characterQuotes;
        private final int image;

        public int getImage() {
            return image;
        }

        public Quote(int image, int name, int quote, CharacterQuote[] characterQuotes) {
            this.image = image;
            this.quote = quote;
            this.characterQuotes = characterQuotes;
            this.name = name;
        }

        public int getName() {
            return name;
        }

        public int getQuote() {
            return quote;
        }

        public CharacterQuote[] getCharacterQuotes() {
            return characterQuotes;
        }

        public static class CharacterQuote {
            private int reputation, nextStep, checkingValue, money, checking, quote;

            public void setReputation(int reputation) {
                this.reputation = reputation;
            }

            public void setNextStep(int nextStep) {
                this.nextStep = nextStep;
            }

            public void setCheckingValue(int checkingValue) {
                this.checkingValue = checkingValue;
            }

            public void setMoney(int money) {
                this.money = money;
            }

            public void setChecking(int checking) {
                this.checking = checking;
            }

            public void setQuote(int quote) {
                this.quote = quote;
            }

            public int getMoney() {
                return money;
            }

            public int getCheckingValue() {
                return checkingValue;
            }

            public int getChecking() {
                return checking;
            }

            public int getQuote() {
                return quote;
            }

            public int getReputation() {
                return reputation;
            }

            public int getNextStep() {
                return nextStep;
            }

            public CharacterQuote(int quote, int nextStep) {
                this.quote = quote;
                this.reputation = 0;
                this.nextStep = nextStep;
                this.checking = -1;
                this.checkingValue = 0;
                this.money = 0;
            }

            public CharacterQuote(int quote, int reputation, int nextStep, int checking, int checkingValue) {
                this.quote = quote;
                this.reputation = reputation;
                this.nextStep = nextStep;
                this.checking = checking;
                this.checkingValue = checkingValue;
                this.money = 0;
            }

            public CharacterQuote(int quote, int reputation, int nextStep) {
                this.quote = quote;
                this.reputation = reputation;
                this.nextStep = nextStep;
                this.checking = -1;
                this.checkingValue = 0;
                this.money = 0;
            }

            public CharacterQuote(int quote, int reputation, int nextStep, int checking, int checkingValue, int money) {
                this.quote = quote;
                this.reputation = reputation;
                this.nextStep = nextStep;
                this.checking = checking;
                this.checkingValue = checkingValue;
                this.money = money;
            }

            public CharacterQuote(int quote, int reputation, int nextStep, int money) {
                this.quote = quote;
                this.reputation = reputation;
                this.nextStep = nextStep;
                this.checking = -1;
                this.checkingValue = 0;
                this.money = money;
            }
        }
    }
